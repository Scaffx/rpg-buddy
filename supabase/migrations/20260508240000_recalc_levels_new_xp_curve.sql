-- Recalcula o level de todos os perfis usando a nova curva de XP quadrática
-- Fórmula idêntica ao progression.ts: incremento(N) = 300·N + 20·N²
-- XP cumulativo por nível:
--   Lv2=320, Lv3=1000, Lv4=2080, Lv5=3600, Lv6=5600, Lv7=8120, Lv8=11200,
--   Lv9=14880, Lv10=19200, Lv11=24200, Lv12=29920, Lv13=36400, Lv14=43680,
--   Lv15=51800, Lv16=60800, Lv17=70720, Lv18=81600, Lv19=93480, Lv20=106400,
--   Lv21=120400, Lv22=135520, Lv23=151800, Lv24=169280, Lv25=188000,
--   Lv26=208000, Lv27=229320, Lv28=252000, Lv29=276080, Lv30=301600,
--   Lv31=328600, Lv32=357120, Lv33=387200, Lv34=418880, Lv35=452200,
--   Lv36=487200, Lv37=523920, Lv38=562400, Lv39=602680, Lv40=644800,
--   Lv41=688800, Lv42=734720, Lv43=782600, Lv44=832480, Lv45=884400,
--   Lv46=938400, Lv47=994520, Lv48=1052800, Lv49=1113280, Lv50=1176000,
--   Lv51=1241000, Lv52=1308320, Lv53=1378000, Lv54=1450080, Lv55=1524600,
--   Lv56=1601600, Lv57=1681120, Lv58=1763200, Lv59=1847880, Lv60=1935200

CREATE OR REPLACE FUNCTION public.get_level_from_xp(p_xp INTEGER)
RETURNS INTEGER AS $$
DECLARE
  xp_table INTEGER[] := ARRAY[
    0, 320, 1000, 2080, 3600, 5600, 8120, 11200, 14880, 19200,
    24200, 29920, 36400, 43680, 51800, 60800, 70720, 81600, 93480, 106400,
    120400, 135520, 151800, 169280, 188000, 208000, 229320, 252000, 276080, 301600,
    328600, 357120, 387200, 418880, 452200, 487200, 523920, 562400, 602680, 644800,
    688800, 734720, 782600, 832480, 884400, 938400, 994520, 1052800, 1113280, 1176000,
    1241000, 1308320, 1378000, 1450080, 1524600, 1601600, 1681120, 1763200, 1847880, 1935200
  ];
  safe_xp INTEGER := GREATEST(0, p_xp);
  i INTEGER;
BEGIN
  FOR i IN REVERSE array_length(xp_table, 1)..2 LOOP
    IF safe_xp >= xp_table[i] THEN
      RETURN i;
    END IF;
  END LOOP;
  RETURN 1;
END;
$$ LANGUAGE plpgsql IMMUTABLE;

-- Recalcula o level de todos os perfis existentes
UPDATE public.profiles
SET level = public.get_level_from_xp(COALESCE(total_xp, 0))
WHERE total_xp IS NOT NULL;

-- Garante que nenhum perfil fique com level NULL ou menor que 1
UPDATE public.profiles
SET level = 1
WHERE level IS NULL OR level < 1;
